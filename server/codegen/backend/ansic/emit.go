// =====================================================================
//  Public entry point
// =====================================================================

// Emit converts an IR program into a set of C99 source files using the
// type decisions captured in the provided profile.
//
// The returned map is keyed by relative path within the exported
// project. The exact set depends on whether the generated body
// touches the IoTMaker runtime:
//
//   - Always present:   main.c
//   - Conditional:      iotmaker_runtime.h, iotmaker_runtime_stub.c
//
// Scenes that never call a runtime symbol (no LoopDuration today)
// receive a single self-contained main.c the maker can compile with
// `gcc main.c -o test` on any host. Scenes that do call a runtime
// symbol receive all three files so the project builds end-to-end
// on a POSIX host out-of-the-box; the stub is swapped for a
// target-specific implementation when deploying to embedded.
//
// The signature is stable from Task 3 onward — every later task adds
// behaviour inside this function without changing what the caller in
// codeGen.go has to pass, protecting the Go path from collateral
// breakage during incremental development.
//
// Português:
//
//	Converte o IR em arquivos C. main.c sempre vai; header e stub
//	só viajam junto quando o corpo gerado realmente usa o runtime
//	(iotmaker_sleep_ns). Cenas simples ficam autocontidas em
//	main.c sozinho.

// generatedCodeHeader is stamped at the top of every emitted file, releasing
// that output from the AGPL copyleft via the IoTMaker Generated Code Exception
// (source of truth: GENERATED-CODE-EXCEPTION.md). C99 accepts // comments, so
// this is kept verbatim-identical to the Go backend's copy.
//
// Português: Carimbado no topo de cada arquivo emitido, liberando essa saída do
// copyleft AGPL pela IoTMaker Generated Code Exception (fonte da verdade:
// GENERATED-CODE-EXCEPTION.md). C99 aceita //, então é idêntico à cópia do
// backend Go.
const generatedCodeHeader = "// Generated by IoTMaker (https://iotmaker.io).\n" +
	"// This generated file is provided under the IoTMaker Generated Code Exception\n" +
	"// and is NOT subject to the AGPL. You may license it as you choose.\n"

// Emit converts the IR program into C files, keyed by ZIP-entry path.
// main.c always ships; the runtime pair travels only when the body uses it;
// each multi-file black-box adds its folder (generated header + authored
// source) and, when at least one exists, the project Makefile.
//
// profile decides the C type family for the target; naming decides the
// generated-name family — radical + code number for folders, files, symbol
// prefixes and guards (see blackbox.Naming). The zero-value Naming uses the
// default radical, so callers without a scene configuration can pass
// blackbox.Naming{}.
//
// Português: Converte o IR em arquivos C (chaves = caminhos de entrada do
// ZIP). profile decide a família de tipos do target; naming decide a família
// de nomes gerados (radical + número). Naming{} usa o radical default.
func Emit(prog *ir.Program, profile TargetProfile, naming blackbox.Naming) map[string]string {
	e := &cEmitter{
		prog:     prog,
		profile:  profile,
		naming:   naming,
		indent:   1, // body lives inside main(); one level of indent
		declared: make(map[string]bool),
	}
	for _, in := range prog.Instructions {
		if in.Op == ir.OpFuncBegin {
			e.hasFuncs = true
			break
		}
	}

	e.emit()

	// Build main.c first — wrapMain reads e.usesRuntime to decide
	// whether to emit the `#include "iotmaker_runtime.h"` line, so
	// it has to run after emit() has walked every instruction.
	out := map[string]string{
		"main.c": e.wrapMain(),
	}

	// The runtime header and stub travel with main.c only when the
	// body actually depends on them. Cenas without LoopDuration
	// produce a single self-contained file the maker can compile
	// with `gcc main.c -o test` — no extra files, no dead
	// dependency. When the body calls iotmaker_sleep_ns, the two
	// extra files come along so the project builds end-to-end on
	// any POSIX host (the stub uses nanosleep), and the maker
	// swaps the stub for a target-specific implementation when
	// deploying to embedded.
	//
	// Português: Header e stub viajam com main.c apenas quando o
	// corpo realmente usa o runtime. Cenas simples saem com
	// main.c sozinho — sem dependências mortas. Quando precisa do
	// sleep, os dois arquivos extras entram pro projeto compilar
	// fora da caixa em qualquer host POSIX.
	if e.usesRuntime {
		out["iotmaker_runtime.h"] = RuntimeHeader
		out["iotmaker_runtime_stub.c"] = RuntimeStub
	}

	// Multi-file black-boxes: one folder per box (generated header + the
	// authored source under its rename preamble). Keys are ZIP-entry paths
	// (forward slashes); the download side creates the folders.
	//
	// The project Makefile ships in BOTH modes since 2026-07 (owner request:
	// the single-file zip must also carry build/run targets). Single-file
	// main.c REMAINS compilable with a bare `gcc main.c` — the Makefile is a
	// convenience layer on top of that contract, never a new requirement.
	//
	// Português: Black-boxes multiarquivo — uma pasta por caixa (header
	// gerado + fonte autoral com preâmbulo). O Makefile do projeto viaja nos
	// DOIS modos desde 2026-07 (pedido do dono: o zip single-file também
	// precisa de alvos build/run). O main.c single-file CONTINUA compilável
	// com `gcc main.c` puro — o Makefile é conveniência em cima desse
	// contrato, nunca uma exigência nova.
	units := e.bbUnits()
	if len(units) > 0 {
		e.bbFiles(units, out)
	}
	out["Makefile"] = e.makefile(units)

	// Stamp the Generated Code Exception header on every GENERATED file so a
	// license scanner reading any single generated file can tell it is released
	// from the AGPL copyleft (source of truth: GENERATED-CODE-EXCEPTION.md).
	// Two kinds of output are exempt, for opposite reasons:
	//
	//   - the black-box .c units — these carry an AUTHOR's source verbatim
	//     under the author's own license; stamping "you may license it as
	//     you choose" over someone else's code would misstate its provenance
	//     (their preamble carries the attribution + licensing note instead).
	//   - Makefile — IS generated and DOES carry the exception, but in its
	//     own `#`-comment dialect (a C-style `/* … */` or `//` header would
	//     break make); see makefile() which stamps it at composition time.
	//
	// Updating existing map values in place is safe here — the range adds and
	// removes no keys.
	//
	// Português: Carimba o header da exceção em cada arquivo GERADO. Duas
	// isenções, por motivos opostos: a unidade .c da black-box é código do AUTOR sob a
	// licença dele (carimbar por cima mentiria a procedência — o preâmbulo
	// carrega a atribuição); o Makefile é gerado e JÁ carrega a exceção em
	// comentário `#` (sintaxe C quebraria o make). Atualizar os valores do
	// map no lugar é seguro — o range não adiciona nem remove chaves.
	for name := range out {
		if name == "Makefile" {
			continue // exception already stamped in `#` dialect by makefile()
		}
		if e.authoredFiles[name] {
			continue // authored source — the author's license governs it
		}
		// After the exception notice, list the black-box authors whose code the
		// project embeds (empty string when there are none). See
		// blackbox.AttributionManifest. Added to every stamped file, mirroring
		// the header itself.
		out[name] = generatedCodeHeader + blackbox.AttributionManifest(prog.BlackBoxDefs) + "\n" + out[name]
	}

	return out
}

// =====================================================================
//  Emitter state
// =====================================================================

// ifFrame tracks the state of one nested if/else block during code
// generation. Pushed onto cEmitter.ifStack by emitIfBegin, peeked by
// emitIfElseSep, popped by emitIfEnd. The three flags determine which
// of the four output forms the if/else takes — see emitIfBegin's
// doc comment for the form table.
//
// This struct intentionally mirrors backend/golang/emit.go's
// ifFrame field-for-field so the two backends evolve in step.
//
// Português:
//
//	Quadro de estado de um bloco if/else aninhado. Empilhado por
//	emitIfBegin, lido por emitIfElseSep, desempilhado por emitIfEnd.
//	Espelho exato do ifFrame do backend Go.
type ifFrame struct {
	hasTrue  bool // true branch has at least one instruction
	hasFalse bool // false branch has at least one instruction
	negated  bool // condition was negated (only false branch has content)
}

// cEmitter holds the per-call state of one Emit invocation: the IR
// being walked, the profile dictating type/literal choices, the
// running body buffer, and the bookkeeping that lets us distinguish
// a variable's first declaration from later assignments to the same
// name.
//
// Why declared is a map of dest names (not of full register refs):
//
// The IR uses a single namespace for destinations within a program.
// Every Dest field — whether it appears as the target of OpConst,
// OpVar, OpAssign, or any arithmetic — is a unique identifier in
// that namespace. The Go backend tracks this identical way (see
// goEmitter.declared) and uses it to decide between `:=` (first use)
// and `=` (reuse). In C the distinction is between a typed
// declaration ("int32_t x = ...;") and a plain assignment
// ("x = ...;"). The map answers that question with O(1) lookup.
//
// ifStack mechanics:
//
// The if/else family of opcodes uses a stack of ifFrame rather than
// a single field so nested if blocks are handled correctly: each
// OpIfBegin pushes a new frame, OpIfElse peeks the top, OpIfEnd
// pops. This is the same shape backend/golang/emit.go uses.
//
// Future state (added by later tasks):
//
//   - includes: a set of headers we know we need (<stdint.h>,
//     <stdbool.h>, "iotmaker_runtime.h"). Today we emit a fixed
//     three; Task 12 will make this dynamic.
//
//   - topLevel: a strings.Builder for declarations that must live
//     OUTSIDE main(), such as the typedef struct definitions for
//     black-box instances. Empty in Phase 1.
//
// Each of these is added when the task that needs it lands; the
// struct stays minimal until then to keep cognitive load low.
//
// Português:
//
//	Estado da emissão atual. declared mapeia nomes de destinos do IR
//	que já foram declarados — usado pra decidir entre "tipo nome =
//	valor;" (primeira vez) e "nome = valor;" (reuso). ifStack rastreia
//	if/else aninhados: cada IfBegin empilha um frame, IfElse consulta
//	o topo, IfEnd desempilha. Estado adicional (includes, topLevel)
//	será introduzido pelas tarefas que precisarem.
type cEmitter struct {
	prog    *ir.Program
	profile TargetProfile
	body    strings.Builder
	indent  int

	// Function lifting — mirror of the Go emitter's cut-region trick
	// (see its field doc); funcs collects `static void <name>(void)`
	// bodies, fileVars the `static` shared declarations. Português:
	// Espelho do truque de recorte do Go — funcs coleta os corpos
	// `static void`, fileVars as declarações compartilhadas.
	hasFuncs     bool
	funcMark     int
	funcName     string
	funcComment  string
	funcPDoc     []string
	funcRDoc     []string
	entryEmitted bool
	funcParams   []funcSigPort
	funcReturns  []funcSigPort
	funcs        strings.Builder
	fileVars     strings.Builder
	declared     map[string]bool
	ifStack      []ifFrame

	// bbSurfaces caches the computed public surface of each multi-file
	// black-box, keyed by BlackBoxDef.ID. Lazily filled by surfaceFor the
	// first time any emitter touches a def (function name, cast text, a
	// declared type, an enum-constant default) — the same surface then
	// serves the bb file assembly in Emit, so the rename defines, the
	// header, and every main.c-side prefix all derive from ONE computation.
	// A def without an ID never enters this map (NewCSurface returns nil):
	// per the BlackBoxDef.ID contract that def has no isolated identity and
	// rides the single-file inline fallback with bare names.
	//
	// Português: Cache da superfície pública por id de black-box, preenchido
	// sob demanda por surfaceFor. Uma computação serve os prefixos do main.c
	// E a montagem das pastas de black-box no Emit. Def sem ID nunca entra —
	// sem identidade isolada, segue o fallback inline com nomes crus.
	bbSurfaces map[string]*blackbox.CSurface

	// naming is the generated-name family for THIS export: radical +
	// composition rules for folders, files, symbol prefixes and guards (see
	// blackbox.Naming). Threaded in through Emit from the scene metadata's
	// ExportPrefix (default radical when unset/invalid), so a maker-
	// configured radical moves every name in the output at once.
	//
	// Português: Família de nomes DESTE export (radical + regras). Chega
	// pelo Emit a partir do ExportPrefix da cena; um botão move todos os
	// nomes de uma vez.
	naming blackbox.Naming

	// authoredFiles records the output-map keys whose content is a
	// black-box AUTHOR's source (the <dir>/<stem>.c units). Filled by
	// bbFiles, read by Emit's stamping loop to exempt them from the
	// Generated Code Exception BY IDENTITY — a name-pattern check broke the
	// moment the folder radical became configurable.
	//
	// Português: Chaves do map cujo conteúdo é fonte do AUTOR (as unidades
	// .c). Preenchido por bbFiles; o carimbo de exceção as isenta POR
	// IDENTIDADE — checagem por padrão de nome quebrou quando o radical
	// virou configurável.
	authoredFiles map[string]bool

	// switchStack tracks nested switch blocks. One bool per open switch:
	// true once a case/default body has been opened (so the next label, or
	// SWITCH_END, knows it must emit a `break;` and dedent first). Pushed on
	// SWITCH_BEGIN, popped on SWITCH_END.
	switchStack []bool

	// condStack tracks nested if/else-if chains (the StatementCase range/
	// comparison lowering). One bool per open chain: true once the first
	// branch is open, so the next COND_LABEL closes the previous branch with
	// `} else if` instead of opening a fresh `if`. Pushed on COND_BEGIN,
	// popped on COND_END. Mirrors switchStack but needs no `break;` — an
	// if/else-if chain is not a switch, so there is no fall-through to stop.
	condStack []bool

	// usesRuntime records whether the emitted body called any
	// function declared in iotmaker_runtime.h. Today the runtime
	// declares a single hook (iotmaker_sleep_ns) used by emitSleep,
	// so this flag is set only by that emitter; adding a new
	// runtime symbol means setting the flag wherever the symbol
	// is emitted.
	//
	// dataBlobs collects the FILE-SCOPE renderings of every DATA_BLOB
	// (maker Data · File / Data · Text instances): big constant byte
	// arrays belong outside main() — and PROGMEM placement is only
	// dependable at file scope on older avr-gcc. wrapMain flushes them
	// (with the shared IOTM helpers block once) right after the
	// includes. Português: Coleta as renderizações em ESCOPO DE ARQUIVO
	// de cada DATA_BLOB: arrays grandes vivem fora do main() — e o
	// PROGMEM só é confiável em escopo de arquivo em avr-gcc antigos. O
	// wrapMain os descarrega (com o bloco de helpers uma vez) logo após
	// os includes.
	dataBlobs []string

	// Read by wrapMain (to gate the `#include` line) and by Emit
	// (to gate the runtime header and stub files in the output
	// map). When false, the generated project consists of main.c
	// alone — a self-contained program that compiles with
	// `gcc main.c` without dragging in the runtime files.
	//
	// Português: Marca se o corpo gerado usa algo do runtime.
	// Setado em emitSleep (única chamada por enquanto). wrapMain e
	// Emit consultam pra decidir se geram o include e os arquivos
	// extras. False = main.c autocontido.
	usesRuntime bool

	// usesStddef records whether the emitted body referenced size_t,
	// which lives in <stddef.h> — neither <stdint.h> nor <stdbool.h>
	// (the two foundational includes) is guaranteed to provide it.
	// Today the only size_t producer is emitConstArray's `_len`
	// companion symbol, so this flag is set only there; any future
	// emitter that writes size_t must set it too.
	//
	// Read by wrapMain to gate the `#include <stddef.h>` line — the
	// same honest-artefact stance as usesRuntime: a header whose
	// symbols never appear in main.c is a dead dependency the maker
	// would have to chase down when extracting the file.
	//
	// Português: Marca se o corpo gerado usa size_t (que vem de
	// <stddef.h>). Hoje só o emitConstArray (símbolo `_len`) seta;
	// wrapMain consulta pra emitir o include apenas quando preciso —
	// mesma filosofia do usesRuntime: include só quando o símbolo
	// realmente aparece no arquivo.
	usesStddef bool

	// usesStdio records whether the emitted body referenced a stdio function —
	// currently only snprintf, emitted for string concatenation (see
	// emitStringConcat). Read by wrapMain to gate `#include <stdio.h>`; neither
	// <stdint.h> nor <stdbool.h> provides snprintf. Same honest-artefact gating
	// as usesStddef: the include appears only when the symbol is actually used.
	//
	// Português: Registra se o corpo emitido usou uma função de stdio — hoje só
	// snprintf, da concatenação de string. Lido pelo wrapMain para emitir
	// `#include <stdio.h>` só quando necessário, mesma filosofia do usesStddef.
	usesStdio bool
}

// =====================================================================
//  Dispatch loop
// =====================================================================

// emit walks the IR instructions and dispatches each opcode to its
// dedicated translator. Opcodes not yet implemented by the current
// phase fall through to the default branch and emit nothing, which
// is the deliberate "fail soft, fail visible" stance for incremental
// development: a scene using an unimplemented opcode produces a
// short main.c rather than a compile error or panic, and the
// missing translation is visible at code-review time.
//
// Why no error for unhandled opcodes:
//
// At this stage, the C backend is known to be incomplete — every
// later task adds opcode coverage. Emitting an error for unhandled
// opcodes would break the "always returns valid C99" contract that
// downstream tooling (zipping, the WASM client's preview) relies on.
// When all opcodes are handled (Task 14 closure), this dispatch may
// optionally add a panic-on-unknown default; for now it stays
// permissive.
//
// Phase 2 status:
//
// Black-box opcodes (OpBBDecl, OpBBProp, OpBBInit, OpBBMethod) appear
// in the IR only when the scene contains black-box instances.
// Validation in codeGen.go rejects scenes with missing BlackBoxDefs,
// so reaching this dispatch with a black-box instruction implies the
// definition was present. The Phase 1 behaviour is identical to
// other unimplemented opcodes: ignore. A future Phase 2 will replace
// these dispatch arms with real emitters.
//
// Português:
//
//	Loop que distribui cada instrução do IR pro tradutor específico.
//	Opcodes não implementados ainda caem no default e emitem nada —
//	contrato "sempre devolve C99 válido", quebra suave durante o
//	desenvolvimento incremental. Black-boxes (Fase 2) também caem
//	aqui por enquanto.
func (e *cEmitter) emit() {
	for _, inst := range e.prog.Instructions {
		// [COMMENT] the maker's device comment (stamped by the IR on the
		// node's first instruction) prefixes it as `// ` lines, one per
		// comment line, at the current indentation.
		// Português: O comentário do device (carimbado pelo IR na primeira
		// instrução do node) o prefixa como linhas `// `, uma por linha do
		// comentário, na indentação corrente.
		if c := inst.Meta["comment"]; c != "" && inst.Op != ir.OpFuncBegin {
			for _, line := range strings.Split(c, "\n") {
				e.writef("// %s\n", strings.TrimRight(line, " \t"))
			}
		}
		switch inst.Op {
		case ir.OpConst:
			e.emitConst(inst)
		case ir.OpFuncBegin:
			e.funcName = inst.Dest
			e.funcMark = e.body.Len()
			// Tunnel-derived signature (Fatia C): params by value,
			// returns as OUT-PARAMS (the black-box pattern) — the
			// function stays void. Português: Assinatura derivada de
			// túneis — parâmetros por valor, retornos como OUT-PARAMS
			// (o padrão das black-boxes); a função segue void.
			e.funcParams = parseFuncSig(inst.Meta["params"])
			e.funcReturns = parseFuncSig(inst.Meta["returns"])
			// The function's own comment and the per-port docs render
			// ABOVE the lifted header (field 2026-07-19: the generic
			// printer dropped the comment inside main's trunk, before
			// the region mark). Português: O comentário da função e os
			// docs de porta renderizam ACIMA do cabeçalho içado (o
			// impressor genérico o derrubava dentro do main).
			e.funcComment = inst.Meta["comment"]
			e.funcPDoc = decodePortDocs(inst.Meta["pdoc"])
			e.funcRDoc = decodePortDocs(inst.Meta["rdoc"])
		case ir.OpFuncEnd:
			region := e.body.String()[e.funcMark:]
			trunk := e.body.String()[:e.funcMark]
			e.body.Reset()
			e.body.WriteString(trunk)
			sig := make([]string, 0, len(e.funcParams)+len(e.funcReturns))
			for _, p := range e.funcParams {
				sig = append(sig, cTypeName(p.typ, e.profile)+" "+p.name)
			}
			for _, r := range e.funcReturns {
				sig = append(sig, cTypeName(r.typ, e.profile)+" *"+r.name)
			}
			args := "void"
			if len(sig) > 0 {
				args = strings.Join(sig, ", ")
			}
			linkage := "static void "
			if e.profile.ProvidesEntryPoints && isArduinoEntry(e.funcName) {
				// Reserved-name entry point (setup/loop/serialEvent):
				// the runtime calls it by EXTERNAL linkage — no static.
				// The signature guard upstream already forbade tunnels
				// on it, so args is "void" here. Português: Ponto de
				// entrada de nome reservado — o runtime chama por
				// linkage EXTERNO, sem static; a guarda de assinatura
				// já proibiu túneis nele.
				linkage = "void "
				e.entryEmitted = true
			}
			for _, line := range docLines(e.funcComment) {
				e.funcs.WriteString("// " + line + "\n")
			}
			// Multiline port notes: EVERY line wears the comment prefix
			// (field 2026-07-19: bare continuation lines broke the C).
			// First line carries "name:", continuations indent under it.
			// Português: Notas multilinha — TODA linha veste o prefixo
			// (linhas de continuação nuas quebravam o C); a primeira
			// leva "name:", as demais indentam sob ela.
			writeDoc := func(name, doc string) {
				lines := docLines(doc)
				for li, line := range lines {
					if li == 0 {
						e.funcs.WriteString("// " + name + ": " + line + "\n")
					} else {
						e.funcs.WriteString("//   " + line + "\n")
					}
				}
			}
			for i, p := range e.funcParams {
				if i < len(e.funcPDoc) {
					writeDoc(p.name, e.funcPDoc[i])
				}
			}
			for i, r := range e.funcReturns {
				if i < len(e.funcRDoc) {
					writeDoc(r.name, e.funcRDoc[i])
				}
			}
			e.funcs.WriteString(linkage + e.funcName + "(" + args + ") {\n")
			e.funcs.WriteString(region)
			e.funcs.WriteString("}\n\n")
			e.funcParams, e.funcReturns = nil, nil
		case ir.OpReturn:
			// Fatia C: each return value lands in its out-param, then a
			// bare return — the function is void by contract. Português:
			// Cada valor de retorno cai no seu out-param; return seco.
			for i, a := range inst.Args {
				if i < len(e.funcReturns) {
					e.writef("*%s = %s;\n", e.funcReturns[i].name, cOperand(a))
				}
			}
			e.writef("return;\n")
		case ir.OpCall:
			e.emitCall(inst)
		case ir.OpVar:
			e.emitVar(inst)
		case ir.OpAssign:
			e.emitAssign(inst)
		case ir.OpDataBlob:
			e.emitDataBlob(inst)
		case ir.OpConstArray:
			e.emitConstArray(inst)
		case ir.OpIndex:
			e.emitIndex(inst)
		case ir.OpAdd, ir.OpSub, ir.OpMul, ir.OpDiv:
			e.emitBinOp(inst)
		case ir.OpConvert:
			e.emitConvert(inst)
		case ir.OpCmpEQ, ir.OpCmpNE, ir.OpCmpLT, ir.OpCmpGT, ir.OpCmpLE, ir.OpCmpGE:
			e.emitCompare(inst)
		case ir.OpLoopBegin:
			e.emitLoopBegin(inst)
		case ir.OpBreakIf:
			e.emitBreakIf(inst)
		case ir.OpLoopEnd:
			e.emitLoopEnd(inst)
		case ir.OpIfBegin:
			e.emitIfBegin(inst)
		case ir.OpIfElse:
			e.emitIfElseSep(inst)
		case ir.OpIfEnd:
			e.emitIfEnd(inst)
		case ir.OpSwitchBegin:
			e.emitSwitchBegin(inst)
		case ir.OpCaseLabel:
			e.emitCaseLabel(inst)
		case ir.OpDefaultLabel:
			e.emitDefaultLabel(inst)
		case ir.OpSwitchEnd:
			e.emitSwitchEnd(inst)
		case ir.OpCondBegin:
			e.emitCondBegin(inst)
		case ir.OpCondLabel:
			e.emitCondLabel(inst)
		case ir.OpCondDefault:
			e.emitCondDefault(inst)
		case ir.OpCondEnd:
			e.emitCondEnd(inst)
		case ir.OpSleep:
			e.emitSleep(inst)
		case ir.OpPrint:
			e.emitPrint(inst)
		case ir.OpBBCall:
			// C99 standalone function-device call. The Go struct black-box
			// opcodes below are still Phase 2 (Go scenes don't reach the C
			// backend), but BB_CALL is C99-only and emitted here.
			e.emitBBCall(inst)
		// Opcodes intentionally never emitted in C:
		//   OpOutput  — display devices are an IDE concept
		//   OpReturn  — Go-only cosmetic no-op
		// Phase 2 (Go struct black-box):
		//   OpBBDecl/Prop/Init/Method                    — Phase 2
		default:
			// Permissive default; see function doc for rationale.
		}
	}
}

