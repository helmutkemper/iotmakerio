// emitCall renders a graphical-function call: every return gets a
// declared out-param local (dead ones are scratch — C needs an lvalue
// for the & either way), then the void call. Português: Chamada de
// função gráfica — todo retorno ganha local declarado (mortos são
// rascunho; C precisa de lvalue no & de qualquer forma), depois a
// chamada void.
func (e *cEmitter) emitCall(inst ir.Instruction) {
	fn := inst.Meta["fn"]
	dests := splitCSV(inst.Meta["dests"])
	types := splitCSV(inst.Meta["types"])
	parts := make([]string, 0, len(inst.Args)+len(dests))
	for _, a := range inst.Args {
		parts = append(parts, cOperand(a))
	}
	for i, d := range dests {
		t := "int"
		if i < len(types) {
			t = types[i]
		}
		e.writef("%s %s;\n", cTypeName(t, e.profile), cIdent(d))
		parts = append(parts, "&"+cIdent(d))
	}
	e.writef("%s(%s);\n", fn, strings.Join(parts, ", "))
}

// splitCSV — empty-safe comma split. Português: Split por vírgula,
// vazio-seguro.
func splitCSV(s string) []string {
	if s == "" {
		return nil
	}
	return strings.Split(s, ",")
}

// funcSigPort is one parsed slot of the FuncBegin signature Meta.
// Português: Um slot decodificado do Meta de assinatura.
type funcSigPort struct{ name, typ string }

// parseFuncSig decodes Meta["params"]/Meta["returns"] ("n1:t1,n2:t2").
// Português: Decodifica o Meta da assinatura.
func parseFuncSig(s string) []funcSigPort {
	if s == "" {
		return nil
	}
	parts := strings.Split(s, ",")
	out := make([]funcSigPort, 0, len(parts))
	for _, p := range parts {
		i := strings.IndexByte(p, ':')
		if i < 0 {
			continue
		}
		out = append(out, funcSigPort{name: p[:i], typ: p[i+1:]})
	}
	return out
}

func (e *cEmitter) emitPrint(inst ir.Instruction) {
	if len(inst.Args) == 0 {
		return
	}
	e.usesStdio = true
	src := cOperand(inst.Args[0])

	lead := ""
	if p := inst.Meta["prefix"]; p != "" {
		lead = cPrintfText(p) + " "
	}

	// [PTR] deref mode: the wire carried a pointer to this scalar. A NULL
	// prints "null pointer" (debug information, not an error); otherwise
	// the pointee is read into src and the normal rendering runs.
	// Português: Modo deref: o fio carregou um ponteiro para este escalar.
	// NULL imprime "null pointer" (informação de debug, não erro); senão o
	// apontado é lido para src e a renderização normal segue.
	closeDeref := false
	if inst.Meta["deref"] == "1" {
		e.writef("if (%s == NULL) {\n", src)
		e.indent++
		e.writef("%s\n", `printf("`+lead+`null pointer\n");`)
		e.indent--
		e.writef("} else {\n")
		e.indent++
		src = "(*" + src + ")"
		closeDeref = true
	}

	var line string
	switch inst.Type {
	case "float":
		if inst.Meta["format"] == "trunc" {
			line = `printf("` + lead + `%ld\n", (long)` + src + `);`
		} else {
			line = `printf("` + lead + `%g\n", (double)` + src + `);`
		}
	case "string":
		line = `printf("` + lead + `%s\n", ` + src + `);`
	case "bool":
		if inst.Meta["format"] == "onezero" {
			line = `printf("` + lead + `%d\n", ` + src + ` ? 1 : 0);`
		} else {
			line = `printf("` + lead + `%s\n", ` + src + ` ? "true" : "false");`
		}
	case "byte":
		if inst.Meta["format"] == "decimal" {
			line = `printf("` + lead + `%u\n", (unsigned)` + src + `);`
		} else {
			line = `printf("` + lead + `0x%02X\n", (unsigned)` + src + `);`
		}
	case "[]byte":
		e.usesStddef = true // the loop counter compares against a size_t
		if inst.Meta["format"] == "text" {
			line = `printf("` + lead + `%.*s\n", (int)` + src + `_len, (const char *)` + src + `);`
		} else {
			if lead != "" {
				e.writef("%s\n", `printf("`+lead+`");`)
			}
			e.writef("%s\n", `for (size_t i = 0; i < `+src+`_len; i++) { printf(i ? " %02X" : "%02X", (unsigned)`+src+`[i]); }`)
			line = `printf("\n");`
		}
	default:
		// "int" and any stray numeric register share the widened-cast path.
		// Português: "int" e qualquer registrador numérico avulso usam o
		// caminho do cast alargado.
		if inst.Meta["format"] == "hex" {
			line = `printf("` + lead + `0x%lX\n", (unsigned long)` + src + `);`
		} else {
			line = `printf("` + lead + `%ld\n", (long)` + src + `);`
		}
	}
	e.writef("%s\n", line)
	if closeDeref {
		e.indent--
		e.writef("}\n")
	}
}

// cPrintfText escapes maker-typed free text for use INSIDE a C printf format
// string: backslashes and double quotes for the C string literal, "%" doubled
// for printf, and a raw newline becomes the two-character escape so the
// output stays a single line. Order matters — backslash first, or the later
// escapes would be double-escaped.
//
// Português: Escapa texto livre do maker para DENTRO de um formato printf C:
// barra e aspas para o literal C, "%" dobrado para o printf, e newline cru
// vira o escape de dois caracteres. A ordem importa — barra primeiro, senão
// os escapes seguintes seriam re-escapados.
func cPrintfText(s string) string {
	s = strings.ReplaceAll(s, "\\", "\\\\")
	s = strings.ReplaceAll(s, "\"", "\\\"")
	s = strings.ReplaceAll(s, "%", "%%")
	s = strings.ReplaceAll(s, "\n", "\\n")
	return s
}

