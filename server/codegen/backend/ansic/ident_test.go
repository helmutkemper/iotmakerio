// TestCTypeName_TimeDurationIsAlwaysInt64 pins the deliberate
// non-profile-dependent mapping of time.Duration. The Go IR carries
// duration values as Go's time.Duration alias, which is always
// int64. In C the same representation is int64_t, regardless of the
// profile's default IntType. Picking int32_t on arduino_uno would
// overflow at one-second cadence (10^9 ns > INT32_MAX).
func TestCTypeName_TimeDurationIsAlwaysInt64(t *testing.T) {
	for _, p := range ListProfiles() {
		got := cTypeName("time.Duration", p)
		want := "int64_t"
		if got != want {
			t.Errorf("cTypeName(\"time.Duration\", %s) = %q, want %q (unconditional int64_t)",
				p.Name, got, want)
		}
	}
}

