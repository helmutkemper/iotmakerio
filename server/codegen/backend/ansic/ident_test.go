// ident_test.go — Unit tests for the pure string helpers in ident.go.
//
// All tests are table-driven where the input space is large enough to
// benefit, and direct otherwise. Each function is exercised against
// the cases the design document calls out plus a small set of
// adversarial inputs (empty strings, leading digits, multiple
// underscore-digit boundaries, unicode garbage).
//
// Where the test pins a behaviour that is intentionally lenient or
// "Phase 2 TODO" (e.g. cIdent does not escape keywords), the test
// asserts the lenient behaviour and a comment marks why — so when
// hardening lands, the test will need to be updated alongside the
// implementation, deliberately.
//
// Português:
//
//	Testes unitários puros para os helpers de ident.go. Casos
//	tabulados onde faz sentido, com edge cases adversariais. Onde
//	o comportamento atual é deliberadamente lento (não escapa
//	keywords, não checa primeiro caractere), o teste pina esse
//	comportamento com um comentário marcando o futuro hardening.

import "testing"

// =====================================================================
//  cIdent
// =====================================================================

func TestCIdent(t *testing.T) {
	cases := []struct {
		name string
		in   string
		want string
	}{
		// Bread-and-butter cases — device IDs as generated by the IoTMaker IDE.
		{"const_int_ide_style", "constInt_1", "constInt1"},
		{"add_ide_style", "add_2", "add2"},
		{"i2c_bus_ide_style", "i2cBus_42", "i2cBus42"},
		{"loop_ide_style", "stmLoop_1", "stmLoop1"},

		// Empty input defaults to "v" — defensive guard, never
		// expected to fire in practice. Matches goIdent.
		{"empty_string_collapses_to_v", "", "v"},

		// No transformation needed.
		{"identity_no_underscores", "alreadyClean", "alreadyClean"},
		{"identity_just_letters", "abc", "abc"},

		// Underscores that don't precede a digit are kept (they are
		// valid C identifier characters and the IoTMaker IDE may
		// use them in custom device names).
		{"underscore_followed_by_letter_kept", "foo_bar", "foo_bar"},
		{"trailing_underscore_kept", "foo_", "foo_"},

		// Multiple underscore-digit boundaries.
		{"two_underscore_digit_groups", "foo_1_bar_2", "foo1_bar2"},

		// Underscore at the very start, followed by digit, is
		// stripped — exposes a leading digit. This is the Phase 2
		// hardening gotcha noted in ident.go. The test pins the
		// current (lenient) behaviour to make any future change
		// explicit.
		{"leading_underscore_digit_strips_to_invalid", "_1foo", "1foo"},

		// Multi-byte characters survive (the byte-level loop only
		// inspects digits and the underscore, both single-byte).
		{"unicode_pass_through", "fóo_1", "fóo1"},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := cIdent(tc.in)
			if got != tc.want {
				t.Errorf("cIdent(%q) = %q, want %q", tc.in, got, tc.want)
			}
		})
	}
}

