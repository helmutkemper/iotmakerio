// TestCLiteral_Int exhaustively maps integer literals across all
// profiles. The suffix from the profile must be appended; negative
// signs survive untouched.
func TestCLiteral_Int(t *testing.T) {
	cases := []struct {
		profile string
		val     string
		want    string
	}{
		{ProfileNameArduinoUno, "10", "10L"},
		{ProfileNameArduinoUno, "0", "0L"},
		{ProfileNameArduinoUno, "-42", "-42L"},
		{ProfileNameArduinoUno, "2147483647", "2147483647L"}, // INT32_MAX
		{ProfileNameCortexM, "10", "10L"},
		{ProfileNamePiLinux, "10", "10LL"},
		{ProfileNamePiLinux, "-42", "-42LL"},
		{ProfileNamePortable, "10", "10L"},
	}

	for _, tc := range cases {
		tc := tc
		t.Run(tc.profile+"_"+tc.val, func(t *testing.T) {
			p := ResolveProfile(tc.profile)
			if got := cLiteral("int", tc.val, p); got != tc.want {
				t.Errorf("cLiteral(\"int\", %q, %s) = %q, want %q",
					tc.val, tc.profile, got, tc.want)
			}
		})
	}
}

// TestCLiteral_Float covers the suffix and the ".0 insertion" gotcha.
// Critical case: a bare integer like "3" passed as a float must
// become "3.0f" (not "3f", which is invalid C).
func TestCLiteral_Float(t *testing.T) {
	cases := []struct {
		profile string
		val     string
		want    string
	}{
		// With decimal point — suffix appends directly.
		{ProfileNameArduinoUno, "3.14", "3.14f"},
		{ProfileNamePiLinux, "3.14", "3.14"}, // empty FloatSuffix

		// Negative.
		{ProfileNameArduinoUno, "-3.14", "-3.14f"},
		{ProfileNamePiLinux, "-3.14", "-3.14"},

		// Exponent counts as "looks like float" — no .0 needed.
		{ProfileNameArduinoUno, "1e10", "1e10f"},
		{ProfileNameArduinoUno, "1.5E-3", "1.5E-3f"},
		{ProfileNamePiLinux, "1e10", "1e10"},

		// The critical case: bare integer used as a float. The C
		// lexer would reject "3f" outright, so we insert ".0"
		// before the suffix.
		{ProfileNameArduinoUno, "3", "3.0f"},
		{ProfileNameArduinoUno, "-7", "-7.0f"},
		{ProfileNamePiLinux, "3", "3.0"}, // also needs ".0" to be a float in C

		// Zero is the same edge.
		{ProfileNameArduinoUno, "0", "0.0f"},
	}

	for _, tc := range cases {
		tc := tc
		t.Run(tc.profile+"_"+tc.val, func(t *testing.T) {
			p := ResolveProfile(tc.profile)
			if got := cLiteral("float", tc.val, p); got != tc.want {
				t.Errorf("cLiteral(\"float\", %q, %s) = %q, want %q",
					tc.val, tc.profile, got, tc.want)
			}
		})
	}
}

// TestCLiteral_ConcreteFloatPrecision covers the literal rendering for a
// StatementConstFloat's explicit precision. float32 gets an unconditional
// "f" suffix (so the literal is a float, not a double the compiler must
// narrow); float64 gets none (an unsuffixed C floating literal is already
// a double). Both share the ".0 insertion" gotcha — a bare integer must
// gain a decimal point or the C lexer rejects it. All profile-independent:
// the precision is the maker's choice, so profile.FloatSuffix is ignored.
func TestCLiteral_ConcreteFloatPrecision(t *testing.T) {
	cases := []struct {
		irType string
		val    string
		want   string
	}{
		// float32 → unconditional "f" suffix.
		{"float32", "3.14", "3.14f"},
		{"float32", "-2.5", "-2.5f"},
		{"float32", "3", "3.0f"}, // bare int gains ".0"
		{"float32", "0", "0.0f"},
		{"float32", "1e10", "1e10f"}, // exponent already looks like float

		// float64 → no suffix (already a double in C).
		{"float64", "3.14", "3.14"},
		{"float64", "-2.5", "-2.5"},
		{"float64", "3", "3.0"}, // bare int gains ".0"
		{"float64", "0", "0.0"},
		{"float64", "1e10", "1e10"},
	}
	// Two profiles with opposite abstract-float suffixes (arduino_uno
	// appends "f", pi_linux appends nothing) prove the concrete cases
	// ignore profile.FloatSuffix entirely.
	for _, profile := range []string{ProfileNameArduinoUno, ProfileNamePiLinux} {
		p := ResolveProfile(profile)
		for _, tc := range cases {
			if got := cLiteral(tc.irType, tc.val, p); got != tc.want {
				t.Errorf("cLiteral(%q, %q, %s) = %q, want %q (profile-independent)",
					tc.irType, tc.val, profile, got, tc.want)
			}
		}
	}
}

