// =====================================================================
//  looksLikeFloat (internal helper, tested indirectly above but worth
//  a few direct cases for documentation)
// =====================================================================

func TestLooksLikeFloat(t *testing.T) {
	cases := []struct {
		in   string
		want bool
	}{
		// Decimal point makes it a float.
		{"3.14", true},
		{".5", true},
		{"3.", true},

		// Exponent markers (lowercase and uppercase) make it a float.
		{"1e10", true},
		{"1E10", true},
		{"1.5e-3", true},

		// No decimal, no exponent — looks like int.
		{"3", false},
		{"-42", false},
		{"0", false},
		{"", false},
	}

	for _, tc := range cases {
		t.Run(tc.in, func(t *testing.T) {
			if got := looksLikeFloat(tc.in); got != tc.want {
				t.Errorf("looksLikeFloat(%q) = %v, want %v", tc.in, got, tc.want)
			}
		})
	}
}
