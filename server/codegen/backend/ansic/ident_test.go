// TestCTypeName_AbstractTypes_RespectsProfile walks every abstract IR
// type through every shipped profile, asserting the concrete C type
// reflects the profile's IntType / FloatType / BoolType choices.
func TestCTypeName_AbstractTypes_RespectsProfile(t *testing.T) {
	profiles := ListProfiles()

	for _, p := range profiles {
		p := p
		t.Run(p.Name, func(t *testing.T) {
			if got := cTypeName("int", p); got != p.IntType {
				t.Errorf("cTypeName(\"int\", %s) = %q, want %q",
					p.Name, got, p.IntType)
			}
			if got := cTypeName("float", p); got != p.FloatType {
				t.Errorf("cTypeName(\"float\", %s) = %q, want %q",
					p.Name, got, p.FloatType)
			}
			if got := cTypeName("bool", p); got != p.BoolType {
				t.Errorf("cTypeName(\"bool\", %s) = %q, want %q",
					p.Name, got, p.BoolType)
			}
		})
	}
}

