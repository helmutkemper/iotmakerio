func TestCOperand(t *testing.T) {
	cases := []struct {
		name string
		in   string
		want string
	}{
		// Simple register references — strip the '%' and apply
		// cIdent's underscore-before-digit rule.
		{"simple_ref", "%constInt_1", "constInt1"},
		{"simple_ref_no_underscore", "%alreadyClean", "alreadyClean"},

		// Compound references (Phase 2 black-box outputs). Two
		// halves: instance ID gets cIdent'd; port name is appended
		// verbatim with an underscore separator. Matches goOperand
		// so paired Go/C output reads identically when debugging.
		{"compound_ref", "%i2cBus_1:bus", "i2cBus1_bus"},
		{"compound_ref_no_underscore_in_instance", "%clock:tick", "clock_tick"},

		// Literals (no '%' prefix) pass through unchanged — they
		// may be string constants like the OUTPUT channel name, or
		// numeric values that bypassed the CONST → register path.
		{"literal_passthrough_number", "42", "42"},
		{"literal_passthrough_string", `"hello"`, `"hello"`},
		{"literal_passthrough_negative", "-3.14", "-3.14"},

		// Edge cases.
		{"empty_string_passes_through", "", ""},
		{"only_percent_collapses_to_v", "%", "v"},
	}

	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			got := cOperand(tc.in)
			if got != tc.want {
				t.Errorf("cOperand(%q) = %q, want %q", tc.in, got, tc.want)
			}
		})
	}
}

