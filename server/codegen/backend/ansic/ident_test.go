// TestCLiteral_TimeDurationAlwaysLL pins the unconditional "LL"
// suffix for duration literals. Unlike int literals (which use the
// profile's IntSuffix — "L" for arduino_uno, "LL" for pi_linux),
// duration is always 64-bit because nanosecond values overflow
// int32 quickly. Using the profile's "L" on arduino_uno would
// truncate the literal at compile time and produce wrong sleep
// durations at runtime — silently. The LL suffix is enforced here
// regardless of profile.
func TestCLiteral_TimeDurationAlwaysLL(t *testing.T) {
	cases := []struct {
		profile string
		val     string
		want    string
	}{
		{ProfileNameArduinoUno, "1000000000", "1000000000LL"}, // 1 sec
		{ProfileNameArduinoUno, "100", "100LL"},
		{ProfileNameCortexM, "1000000000", "1000000000LL"},
		{ProfileNamePiLinux, "1000000000", "1000000000LL"}, // same as int64 here
		{ProfileNamePortable, "500000", "500000LL"},
	}

	for _, tc := range cases {
		tc := tc
		t.Run(tc.profile+"_"+tc.val, func(t *testing.T) {
			p := ResolveProfile(tc.profile)
			if got := cLiteral("time.Duration", tc.val, p); got != tc.want {
				t.Errorf("cLiteral(\"time.Duration\", %q, %s) = %q, want %q (LL unconditional)",
					tc.val, tc.profile, got, tc.want)
			}
		})
	}
}

