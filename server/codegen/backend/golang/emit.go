func (e *goEmitter) writef(format string, args ...interface{}) {
	for i := 0; i < e.indent; i++ {
		e.body.WriteString("\t")
	}
	fmt.Fprintf(&e.body, format, args...)
}

func (e *goEmitter) wrapMain() string {
	var sb strings.Builder
	// Generated Code Exception header first, then the mandatory blank line
	// before `package` — without it Go binds the comment as the package
	// doc-comment instead of a standalone notice.
	// Português: header da exceção primeiro, e a linha em branco obrigatória
	// antes de `package` — sem ela o Go trata o comentário como doc-comment do
	// pacote, não como aviso independente.
	sb.WriteString(generatedCodeHeader)
	// After the exception notice, list the black-box authors whose code this
	// file embeds (empty string when there are none, e.g. the scene uses only
	// the maker's own code). See blackbox.AttributionManifest.
	sb.WriteString(blackbox.AttributionManifest(e.prog.BlackBoxDefs))
	sb.WriteString("\npackage main\n\n")

	// Imports (merged: native + black-box)
	if len(e.imports) > 0 {
		if len(e.imports) == 1 {
			for imp := range e.imports {
				sb.WriteString(fmt.Sprintf("import %q\n\n", imp))
			}
		} else {
			sb.WriteString("import (\n")
			// Sort for deterministic output
			sorted := make([]string, 0, len(e.imports))
			for imp := range e.imports {
				sorted = append(sorted, imp)
			}
			sortStrings(sorted)
			for _, imp := range sorted {
				sb.WriteString(fmt.Sprintf("\t%q\n", imp))
			}
			sb.WriteString(")\n\n")
		}
	}

	// Top-level code (black-box struct definitions and methods)
	if e.topLevel.Len() > 0 {
		sb.WriteString(e.topLevel.String())
	}

	if e.fileVars.Len() > 0 {
		sb.WriteString("// ── Shared state (crosses function boundaries) ──\n")
		sb.WriteString(e.fileVars.String())
		sb.WriteString("\n")
	}
	if e.funcs.Len() > 0 {
		sb.WriteString(e.funcs.String())
	}

	// main function
	sb.WriteString("func main() {\n")
	sb.WriteString(e.body.String())
	sb.WriteString("}\n")
	return sb.String()
}

