// reading any single generated file can then tell it is free to license as the
// user chooses. Kept verbatim-identical to the C99 backend's copy.
//
// Português: Carimbado no topo de cada arquivo que o gerador emite, liberando
// essa saída do copyleft AGPL pela IoTMaker Generated Code Exception (fonte da
// verdade: GENERATED-CODE-EXCEPTION.md). Mantido idêntico byte-a-byte à cópia
// do backend C99.
